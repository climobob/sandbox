      SUBROUTINE GISYS(CNSYS,CDATE,CTIME)
C$$$  SUBPROGRAM DOCUMENTATION BLOCK
C
C SUBPROGRAM: GISYS          GET SYSTEM INFORMATION FOR GRBINDEX
C   PRGMMR: IREDELL          ORG: NP23        DATE: 95-10-31
C
C ABSTRACT: THIS SUBPROGRAM RETURNS SYSTEM INFORMATION, SPECIFICALLY
C   THE CURRENT SYSTEM NAME AND DATE AND TIME.
C
C PROGRAM HISTORY LOG:
C   95-10-31  IREDELL
C
C USAGE:    CALL GISYS(CNSYS,CDATE,CTIME)
C   OUTPUT ARGUMENTS:
C     CNSYS        CHARACTER*15 CURRENT SYSTEM NAME
C     CDATE        CHARACTER*10 CURRENT DATE IN YYYY-MM-DD FORM
C     CTIME        CHARACTER*10 CURRENT TIME IN HH:MM:SS FORM
C
C SUBPROGRAMS CALLED:
C   UNAME           GET CURRENT SYSTEM NAME
C   DATE_AND-TIME   GET CURRENT DATE AND TIME
C
C REMARKS: THIS IS A MACHINE-DEPENDENT SUBPROGRAM.
C
C ATTRIBUTES:
C   LANGUAGE: FORTRAN 90
C
C$$$
      CHARACTER CNSYS*15,CDATE*10,CTIME*8
      CHARACTER CD8*8,CT10*10,CSYS*8,CNODE*8,CREL*8,CVER*8,CMACH*8
C - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
      CALL UNAME(CSYS,CNODE,CREL,CVER,CMACH)
      CNSYS=CSYS(1:7)//' '//CREL(1:7)
      CALL DATE_AND_TIME(CD8,CT10)
      CDATE=CD8(1:4)//'-'//CD8(5:6)//'-'//CD8(7:8)
      CTIME=CT10(1:2)//':'//CT10(3:4)//':'//CT10(5:6)
C - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
      RETURN
      END
