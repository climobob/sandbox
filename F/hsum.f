      SUBROUTINE HSUM(PHIU,PHIL,TMP,XALFA,XBETA,NMET,IX1,IXM,K)
C
C  THIS ROUTINE VECTORIZE HYDROSTATIC EQUATION
C
      DIMENSION PHIU(NMET*K),PHIL(NMET*K)
      DIMENSION TMP(NMET*K)
      DIMENSION XALFA(NMET),XBETA(NMET)
C
      DO 10 NM=1,NMET
      PHIU(NM)=PHIL(NM)+XALFA(K)*TMP(NM+IX1)+XBETA(K-1)*TMP(NM+IXM)
   10 CONTINUE
C
      RETURN
      END
