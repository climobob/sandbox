      SUBROUTINE ISEA 
C     TAPE16 OPENED HERE 8/30/83 TO MAKE ISEA MORE SELF CONTAINED.
C     SAVE THE INITIAL CONDITIONS FOR SEA ICE AND RESTORE EACH CYCLE
C     ISEA 10/16/79 
      COMMON W(46,50) 
      COMMON/LARGE/NFLD,JFLD(12,2)
      COMMON/MINOR/NMINOR,KFLD(26)
      COMMON/CONST/M,MM,MP,MPP,N,DUMA(42) 
      REAL GRDX(50) 
      DATA IU/16/,KEY/0/
      JXIO=JFLD(10,2) 
      CALL READMS(IU,W(1,JXIO),MPPSV,KEY) 
      RETURN
      ENTRY ISEASET 
      KEY=NFLD+NMINOR+5 
      MPPSV=MPP 
      JXIO=JFLD(10,2) 
      CALL OPENMS(IU,GRDX,50,0) 
      CALL WRITMS(IU,W(1,JXIO),MPPSV,KEY,-1,0)
      RETURN
      END 
