      SUBROUTINE CYCSUB(NSA,NSB,NSCRD,NSCPR,NSZ,N1,N2,N3,N4)
C     CYCSUB FINDS START AND END TIME STEPS AND OTHER COUNTERS FOR TWOB.
C     THE OPENING OF TAPE16 MOVED TO ISEASET 8/29/83. 
      COMMON/CFCOM/NCG,NSC
 41   FORMAT(1H1,    /
     1      13H  START  SIZE  /1X,4I5,10X,10HCP SECONDS  ,F10.3)
CD      WRITE(6,41)N1,N2,N3,N4,SECOND() 
      WRITE(6,41)N1,N2,N3,N4
      IF (N1.NE.88888) NSZ=N1 
      N2   =N2*NCG
      NSA  =NSZ+NSC 
      NSB  =NSZ+N2
      NSCRD=N3*NSC
      NSCPR=N4*NSC
  
      RETURN
      END 
