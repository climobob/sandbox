      SUBROUTINE ZEROSL(LS, LL, KRAT, FS, FL) 
C     TO USE IN CLEANING UP INTERPLATION
C     SETS ZERO VALUES INTERPOLATED TO THE LONG TS GRID IF THE
C     ADJACENT VALUES ON THE SHORT TS GRID ARE BOTH ZERO. 
  
      REAL  FS(LS), FL(LL)
  
      IF (KRAT.LE.1) RETURN 
      LSM = LS-1
      DO 10 LA = 1, LSM 
        DO 20 K = 2, KRAT 
C         LAP1 INTRODUCED TO AVOID ERRORS WITH FTN5 AT OPT = 2. 
          LAP1 = LA+1 
          KK  = K+(LA-1)*KRAT 
          IF ((FS(LA).EQ.0.0).AND.(FS(LAP1).EQ.0.0)) FL(KK) = 0.0 
  20    CONTINUE
  10  CONTINUE
  
      RETURN
      END 
