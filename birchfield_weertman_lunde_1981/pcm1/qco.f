       FUNCTION QCO(T,H,XI) 
C      Albert Lunde 1981

       COMMON/CONST/
     1 M,MM,MP,MPP,N, 
     2 DELPHI,DELT,PI,AEARTH,CA,
     3 CP,CO,CD,P00,P0, 
     4 SIGMA1,SIGMA2,XKAPPA,SIG,XLC,
     5 XLF,RHOW,RHOS,DIFF,XKS,
     6 XKI,XIMIN,SC,
     7 ITMAX,XLAM,XLS,PWLF, 
     8 PWLS,CDPW,CDCP,CDLC,CDLS,
     9 PWPS,XKSKI,RSEA,SGKP1,SGKP2, 
     1 ZSGKPA,ZSGKPB,CLRE,SQ1,SQ2 
C      HEAT CONDUCTION THRU SNOW H + ICE XI TO TEMP T 
       QCO = XKS * (273. - T)/(PWPS * H + XKSKI * (XI + XIMIN)) 
       RETURN 
       END      
