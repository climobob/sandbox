      SUBROUTINE HLZSET(NSZ)
C     ADD DHLZ ACC.RATE COMPUTATIONS    11/23/79
  
      COMMON W(46, 50)
      COMMON/CONST/M, MM, MP, MPP, N, DELPHI, DELT, DUM(40) 
C     COMMON CFCOM REMOVED FROM HLZSET 8/31/83, UNUSED HERE.
      COMMON/AV3COM/JAVE, JAVEON, MZERO, MPREF, LHLS, LHOS, LXIOS 
      COMMON/MINOR/NMINOR, JDUM(21), JDHLZ, DUMMA(4)
  
      REAL DHLSZ(23)
  
      IF(JAVE.NE.JAVEON) RETURN 
      NSZOLD = NSZ
      DO 10 MZ = 1, MPP 
        W(MZ, JDHLZ) = 0.0
  10  CONTINUE
      RETURN
  
C     CONVERT RAW SUM INTO ACC RATES
      ENTRY HLZCON(NSZ) 
      IF(JAVE.NE.JAVEON) RETURN 
      LA = MZERO
      LB = MZERO+LHLS-1 
      DO 20 MZ = LA, LB 
        W(MZ, JDHLZ) = W(MZ, JDHLZ)/(NSZ-NSZOLD)
  20  CONTINUE
      RETURN
  
      ENTRY HLZOUT(DHLSZ) 
      L  = 0
      LA = MZERO
      LB = MZERO+LHLS-1 
      DO 30 MZ = LA, LB 
        L = L+1 
        DHLSZ(L) = W(MZ, JDHLZ) 
  30  CONTINUE
  
      RETURN
  
      END 
